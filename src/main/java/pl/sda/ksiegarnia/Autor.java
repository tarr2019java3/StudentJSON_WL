package pl.sda.ksiegarnia;

public class Autor {

    private String name;
    private String lastname;

    public Autor() {

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }
}
